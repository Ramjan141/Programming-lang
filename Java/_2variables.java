public class _2variables {
    public static void main(String[] args) {
        int a = 5;
        int b = 7;
        String name = "Ramjan Ansari";

        System.out.println(a);
        System.out.println(b);
        System.out.println(name);

        a = 55;
        System.out.println(a);// Value replace with new value.

        a = b;
        System.out.println(a);// value replace with b value.
    }
}
