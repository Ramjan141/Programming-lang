public class _12switchCase {
    public static void main(String[] args) {
        int num = 4;

        switch (num) {
            case 1:
                System.out.println("Samosa");
                break;
            case 2:
                System.out.println("Burger");
                break;
            case 3:
                System.out.println("Mango");
                break;
        
            default:
                    System.out.println("Wrong Number");
                break;
        }
    }
}
