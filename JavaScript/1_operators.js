let a = 10;
let b = 7;

let c = true; //boolean type
let d = false;//boolean type

console.log(c);
console.log(d);


// Arithmetic operators------------------------>
console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log("power 2^3", 2 ** 3);

// Unary operators------------------------------->

// Post-increment
age = 5;
console.log(age++);
console.log(age);

//pre-increment
console.log(++age);
console.log(age);

// post-decrement
age = 5;
console.log(age--);
console.log(age);

// Pre-decrement
console.log(--age);
console.log(age);

// Assignment Operators-------------------------->
// like(=,+=,-=,*=,/=.%= etc.)                              
let x = 3;
let y = x; // assigns the value of x in y

console.log("The value of X is :", x);
console.log("The value of Y is :", y);

// Comparision Operators------------------------->
// Like(>,>=,<,<=,==,!=,===)

let age2 = 18;
console.log(age2 >= 18);
console.log(age2 <= 18);
console.log(age2 != 18);
console.log(age2 == 18);


// Logical Operators------------------------------->
// Like(&&,||,!)

// Logical AND (&&)
console.log(true && true);
console.log(true && false);
console.log(false && true);
console.log(false && false);

// Logical OR (||)
console.log(true || false);
console.log(true || true);
console.log(false || true);
console.log(false || false);

// Logical NOT (!)
console.log(!true);
console.log(!false);